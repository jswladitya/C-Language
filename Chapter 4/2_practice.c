#include<stdio.h>
//write a program to print table of 10 in reverse order
int main(){
    for(int i = 10 ; i ; i--){
        printf("10 x %d = %d\n", i , 10*i);
        //here pehle %d is i and dusra %d will is 10*i
    }
    return 0;
}


